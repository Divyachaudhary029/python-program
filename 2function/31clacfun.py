x=int(input("enter number"))
y=int(input("enter number"))


def add(x,y):
    return x+y
def subs(x,y):
    return x-y
def mul(x,y):
    return x*y
def div(x,y):
    return x/y
def mod(x,y):
    return x%y
def power(x,y):
    return x**y
def divfloor(x,y):
    return x//y

a=add(x,y)
b=subs(x,y)
c=mul(x,y)
d=div(x,y)
e=mod(x,y)
f=power(x,y)
